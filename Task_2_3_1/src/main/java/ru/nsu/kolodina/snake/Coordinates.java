package ru.nsu.kolodina.snake;

/**
 * Represents coordinates (x, y) on the game field.
 */
public class Coordinates {
    int x, y;

    /**
     * Create coordinates.
     *
     * @param x x-coordinate
     * @param y y-coordinate
     */
    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
